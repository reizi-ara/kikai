//スクロール関数
//Point *_pos		プレイヤー座標
//vector *_scroll	プレイヤーのスクロール情報
//int _WIN_ID		WINDOW_ID
void GetScroll(Point* _pos, Vector* _scroll, int _WIN_ID)
{
	//スクロール保存用変数
	_scroll->x = 0.0f;
	_scroll->y = 0.0f;

	//X軸スクロールライン
	if (_pos->x + IMGSIZE64 >= SCROLL_LINE_X)
	{
		_scroll->x = _pos->x + IMGSIZE64 - SCROLL_LINE_X;
	}
	if (_pos->x + IMGSIZE64 >= SCROLL_LINE_X_MAX)
	{
		_scroll->x = SCROLL_LINE_X_MAX - SCROLL_LINE_X;
	}
	//Y軸スクロールライン
	if (_pos->y >= SCROLL_LINE_Y)
	{
		_scroll->y = _pos->y - SCROLL_LINE_Y;
	}
	if (_pos->y >= SCROLL_LINE_Y_MAX)
	{
		_scroll->y = SCROLL_LINE_Y_MAX - SCROLL_LINE_Y;
	}

	//WINDOWにあわせた位置を調整
	switch (_WIN_ID)
	{
	case P1:
		break;
	case P2:
		_scroll->x + 928.0f;
		break;
	case P3:
		_scroll->y + 508.0f;
		break;
	case P4:
		_scroll->x + 928.0f;
		_scroll->y + 508.0f;
		break;
	}

}


//MAP情報取得関数
//_map[MAP_SIZE_Y][MAP_SIZE_X]	保存するmap変数
void GetMap(int _map[MAP_SIZE_Y][MAP_SIZE_X])
{
	for (int y = 0; y < MAP_SIZE_Y; y++)
	{
		for (int x = 0; x < MAP_SIZE_Y; x++)
		{
			_map[y][x] = map[y][x];
		}
	}
}

//ブロックの当たり判定
void BlockHit(Hit* player, Point* _pos, Vector* _vec)
{
	//マップ
	int map[MAP_SIZE_Y][MAP_SIZE_X];
	//マップ読み込み
	GetMap(map);

	//当たり判定初期化 重要!!!!!!!!
	player->UP = false;
	player->DOWN = false;
	player->LEFT = false;
	player->RIGHT = false;

	//マップチップから当たっているかを探す
	for (int y = 0; y < MAP_SIZE_Y; y++) {
		for (int x = 0; x < MAP_SIZE_X; x++) {
			//ブロック
			if (map[y][x] == 1)
			{
				//キャラクターの右側が当たった時
				if ((x * IMGSIZE64) + IMGSIZE64 >= _pos->x + IMGSIZE64 &&
					(x * IMGSIZE64) - IMGSIZE64 <= _pos->x &&
					y * IMGSIZE64 - 1 < _pos->y + (IMGSIZE64 / 2) &&
					(y * IMGSIZE64) + IMGSIZE64 > _pos->y + (IMGSIZE64 / 2))
				{
					_pos->x = x * IMGSIZE64 - IMGSIZE64;//位置を右側にする 
					player->RIGHT = true;
				}
				//キャラクターの左側が当たった時
				if ((x * IMGSIZE64) <= _pos->x &&
					(x * IMGSIZE64) + (IMGSIZE64 * 2) >= _pos->x + IMGSIZE64 &&
					y * IMGSIZE64 - 1 < _pos->y + (IMGSIZE64 / 2) &&
					(y * IMGSIZE64) + IMGSIZE64 > _pos->y + (IMGSIZE64 / 2))
				{
					_pos->x = x * IMGSIZE64 + IMGSIZE64;//位置を左側にする
					player->LEFT = true;
				}
				//キャラクターの上側が当たった時
				if ((y * IMGSIZE64) + IMGSIZE64 >= _pos->y &&
					(y * IMGSIZE64) <= _pos->y &&
					(x * IMGSIZE64) + IMGSIZE64 >= _pos->x + (IMGSIZE64 / 2) &&
					(x * IMGSIZE64) <= _pos->x + (IMGSIZE64 / 2))
				{
					_pos->y = (y * IMGSIZE64) + IMGSIZE64;//位置をブロックの上にする
					player->UP = true;
				}
				////キャラクターの下側が当たった時
				if ((y * IMGSIZE64) >= _pos->y &&
					(y * IMGSIZE64) - (IMGSIZE64) <= _pos->y &&
					(x * IMGSIZE64) + IMGSIZE64 >= _pos->x + (IMGSIZE64 / 2) &&
					(x * IMGSIZE64) <= _pos->x + (IMGSIZE64 / 2))
				{
					_pos->y = (y * IMGSIZE64) - IMGSIZE64 - 2;//位置をブロックの上にする
					player->DOWN = true;
				}
			}
		}

	}
}